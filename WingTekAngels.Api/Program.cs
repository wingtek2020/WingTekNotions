using FastEndpoints;
using FastEndpoints.Swagger;
using Microsoft.EntityFrameworkCore;
using WingTekAngels.Api.Data;

namespace WingTekAngels.Api
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // ???????????????????????????????????????
            // EF Core
            // ???????????????????????????????????????
            builder.Services.AddDbContext<AngelsDbContext>(options =>
                options.UseSqlServer(builder.Configuration.GetConnectionString("DefaultConnection")));

            // ???????????????????????????????????????
            // Auth (if you need it later)
            // ???????????????????????????????????????
            builder.Services.AddAuthorization();

            // ???????????????????????????????????????
            // FastEndpoints + Swagger (ONLY THESE)
            // ???????????????????????????????????????
            builder.Services.AddFastEndpoints();
            builder.Services.SwaggerDocument(o =>
            {
                o.DocumentSettings = s =>
                {
                    s.Title = "WingTek Angels API";
                    s.Version = "v1";
                };
            });

            builder.Services.AddCors(options =>
            {
                options.AddDefaultPolicy(policy =>
                {
                    policy
                        .WithOrigins("http://localhost:4200") // Angular dev URL
                        .AllowAnyHeader()
                        .AllowAnyMethod();
                });
            });

            var app = builder.Build();

            // ???????????????????????????????????????
            // Pipeline
            // ???????????????????????????????????????
            app.UseHttpsRedirection();

            app.UseCors();

            app.UseAuthorization();

            // register endpoints
            app.UseFastEndpoints();

            // register swagger UI + JSON
            app.UseSwaggerGen();

            app.Run();
        }
    }
}
